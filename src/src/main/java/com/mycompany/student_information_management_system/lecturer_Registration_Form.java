    private void btnLecturerRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLecturerRegisterActionPerformed
        // TODO add your handling code here:
            String firstName=txtLecFirstName.getText();
            String lastName=txtLecLastName.getText();;
            String email=txtEmail.getText();
            String phoneNumber=txtPhoneNumber.getText();
            String address=txtAddress.getText();
            String lectureCourse=cbxCourses.getSelectedItem().toString();
            String lecCourseModules=cbxCourseModules.getSelectedItem().toString();
            String EmployeeID=txtEmployeeID.getText();
            String password=txtPassword.getText();
            
            

            if (txtLecFirstName.getText().isEmpty() || txtLecLastName.getText().isEmpty() || txtEmail.getText().isEmpty() || txtPhoneNumber.getText().isEmpty() || txtAddress.getText().isEmpty() || cbxCourses==null || cbxCourseModules==null || txtPassword.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "All fields are required!");
            } 
            else {
                
            
        
            
                try {
                    


                
                String lecturer_addQuery = "INSERT INTO lecture_table(FirstName, LastName, Email, PhoneNumber, Address, teachingCourse, CourseModule, EmployeeIDNumber, password) VALUES(?,?,?,?,?,?,?,?,?);";

                pst=con.prepareStatement(lecturer_addQuery);

                pst.setString(1,firstName);
                pst.setString(2,lastName);
                pst.setString(3,email);
                pst.setString(4,phoneNumber);
                pst.setString(5,address);
                pst.setString(6,lectureCourse);
                pst.setString(7,lecCourseModules);
                pst.setString(8,EmployeeID);
                pst.setString(9,password);

                int x=pst.executeUpdate();
                if(x==1)
                {
                    JOptionPane.showMessageDialog(this,"The Lecturer Register successfully Successfully!!");
                    txtLecFirstName.setText("");
                    txtLecLastName.setText("");
                    txtEmail.setText("");
                    txtPhoneNumber.setText("");
                    txtAddress.setText("");
                    txtEmployeeID.setText("");
                    txtPassword.setText("");
                    
                }
                else
                {
                    JOptionPane.showMessageDialog(this,"Record failed to saved!!");
                }
        } 
        catch (SQLException ex) {
            Logger.getLogger(lecturer_Registration_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_btnLecturerRegisterActionPerformed

