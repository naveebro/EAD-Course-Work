    public void loadCourses()
    {
        String loadCourses_query="SELECT courseID FROM coursesdetails_table;";
        try {
            pst=con.prepareStatement(loadCourses_query);
            rs=pst.executeQuery();
            cbxCourses.removeAllItems();
            
            while(rs.next())
            {
                cbxCourses.addItem(rs.getString(1));
            }
        } 
        catch (SQLException ex) {
            Logger.getLogger(lecturer_Registration_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
