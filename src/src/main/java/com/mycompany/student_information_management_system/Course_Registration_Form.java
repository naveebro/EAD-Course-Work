    private void btnAddCourseRequirementsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCourseRequirementsActionPerformed
        if(txtCourseID.getText().isEmpty()||txtCourseRequirements.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this,"CourseID and Course Requirements fields needs to be filled");
        }
        else{
        try {
            // TODO add your handling code here:
            String courseRequirements=txtCourseRequirements.getText();
            String courseID=txtCourseID.getText();
            
            String CourseRequirement_addQuery="INSERT INTO courserequirementstable(courseID,courseRequirements) VALUES(?,?);";
            pst=con.prepareStatement(CourseRequirement_addQuery);
            
            pst.setString(1,courseID);
            pst.setString(2,courseRequirements);
            
            
            int x=pst.executeUpdate();
            if(x==1)
            {
                JOptionPane.showMessageDialog(this,"Course Requirements Added Successfully!!");
                
                txtCourseRequirements.setText("");
   
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Course failed to saved!!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Course_Registration_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_btnAddCourseRequirementsActionPerformed

