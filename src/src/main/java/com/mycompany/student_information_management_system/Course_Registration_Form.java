    private void btnRegisterCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterCourseActionPerformed
        // TODO add your handling code here:
        if(txtCourseID.getText().isEmpty()||txtCourseName.getText().isEmpty()||cbxCourseCategory.getSelectedItem()==null||spnr_NoOfYearsOfCourse.equals("Select an option")||spnr_NoOfMonthsOfCourse.equals("Select an option")||txtCourseFee.getText().isEmpty()||cbx_Location.getSelectedItem()==null)
        {
            JOptionPane.showMessageDialog(this,"All fields needs to be validate");
        }
        else
        {
        try {
            
            String courseID=txtCourseID.getText();
            String courseName=txtCourseName.getText();
            String courseCategory=cbxCourseCategory.getSelectedItem().toString();
            int no_Of_years=(int)spnr_NoOfYearsOfCourse.getValue();
            int no_Of_months=(int)spnr_NoOfMonthsOfCourse.getValue();
            int CourseFee = Integer.parseInt(txtCourseFee.getText());
            String location=cbx_Location.getSelectedItem().toString();
            
            String Course_addQuery="INSERT INTO coursesdetails_table(courseID,course_Name,category,years,months,course_Fee,location) VALUES(?,?,?,?,?,?,?);";
            pst=con.prepareStatement(Course_addQuery);
            
            pst.setString(1,courseID);
            pst.setString(2,courseName);
            pst.setString(3,courseCategory);
            pst.setString(4,String.valueOf(no_Of_years));
            pst.setString(5,String.valueOf(no_Of_months));
            pst.setString(6,String.valueOf(CourseFee));
            pst.setString(7,location);
            
            int x=pst.executeUpdate();
            if(x==1)
            {
                JOptionPane.showMessageDialog(this,"Course Added Successfully!!");
                txtCourseID.setText("");
                txtCourseName.setText("");
                txtCourseRequirements.setText("");
                txtCourseFee.setText("");

                
                
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Course failed to saved!!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Course_Registration_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_btnRegisterCourseActionPerformed

